const TABLE_HEAD = [
  { id: '1', label: 'Order Number', alignRight: false },
  { id: '2', label: 'Location', alignRight: false },
  { id: '3', label: 'Delivery Date', alignRight: false },
  { id: '4', label: 'Order Status', alignRight: false }
];

